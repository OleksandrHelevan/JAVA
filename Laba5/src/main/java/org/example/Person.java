package org.example;

public class Person {
    String name;
    String surname;
    String middleName;
    String dateOfBirth;

    public Person(String name, String surname, String middleName, String dateOfBirth) {
        this.name = name;
        this.surname = surname;
        this.middleName = middleName;
        this.dateOfBirth = dateOfBirth;
    }

}
